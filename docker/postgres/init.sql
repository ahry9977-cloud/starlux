-- Indexes + FTS (PostgreSQL)

-- stores
CREATE INDEX IF NOT EXISTS stores_sellerid_idx ON stores(sellerid);
CREATE INDEX IF NOT EXISTS stores_isactive_idx ON stores(isactive);
CREATE INDEX IF NOT EXISTS stores_createdat_idx ON stores(createdat);
CREATE INDEX IF NOT EXISTS stores_name_trgm_idx ON stores USING GIN (name gin_trgm_ops);
CREATE INDEX IF NOT EXISTS stores_desc_trgm_idx ON stores USING GIN (description gin_trgm_ops);
CREATE INDEX IF NOT EXISTS stores_fts_idx ON stores USING GIN (
  to_tsvector('simple', coalesce(name,'') || ' ' || coalesce(category,'') || ' ' || coalesce(description,''))
);

-- categories
CREATE INDEX IF NOT EXISTS categories_parentid_idx ON categories(parentid);
CREATE INDEX IF NOT EXISTS categories_isactive_idx ON categories(isactive);
CREATE INDEX IF NOT EXISTS categories_featured_idx ON categories(isfeatured);
CREATE INDEX IF NOT EXISTS categories_namear_trgm_idx ON categories USING GIN (namear gin_trgm_ops);
CREATE INDEX IF NOT EXISTS categories_nameen_trgm_idx ON categories USING GIN (nameen gin_trgm_ops);
CREATE INDEX IF NOT EXISTS categories_fts_idx ON categories USING GIN (
  to_tsvector('simple', coalesce(namear,'') || ' ' || coalesce(nameen,'') || ' ' || coalesce(description,''))
);

-- products
CREATE INDEX IF NOT EXISTS products_storeid_idx ON products(storeid);
CREATE INDEX IF NOT EXISTS products_categoryid_idx ON products(categoryid);
CREATE INDEX IF NOT EXISTS products_isactive_idx ON products(isactive);
CREATE INDEX IF NOT EXISTS products_createdat_idx ON products(createdat);
CREATE INDEX IF NOT EXISTS products_title_trgm_idx ON products USING GIN (title gin_trgm_ops);
CREATE INDEX IF NOT EXISTS products_desc_trgm_idx ON products USING GIN (description gin_trgm_ops);
CREATE INDEX IF NOT EXISTS products_fts_idx ON products USING GIN (
  to_tsvector('simple', coalesce(title,'') || ' ' || coalesce(description,''))
);

-- orders
